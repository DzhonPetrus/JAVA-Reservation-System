    public void loadALLACCOUNTS(){
        List<CLASS_ACCOUNTS> accounts = null;
        accounts = daoACCOUNTS.getAllAccounts();
        TableModelACCOUNTS model = new TableModelACCOUNTS(accounts);
        tblACCOUNTS.setModel(model);
        
        newID = accounts.get(accounts.size()-1).getId() + 1;
        txtID.setText(Integer.toString(newID));
    }
    
    public void loadALLTRANSACTIONS(){
        List<CLASS_TRANSACTIONS> transactions = null;
        transactions = daoTRANSACTIONS.getAllTransactions();
        TableModelTRANSACTIONS model = new TableModelTRANSACTIONS(transactions);
        tblTRANS.setModel(model);
    }
    
