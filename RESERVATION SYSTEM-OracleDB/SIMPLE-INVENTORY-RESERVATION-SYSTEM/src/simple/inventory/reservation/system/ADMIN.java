    private void btnACTIONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnACTIONActionPerformed
        // TODO add your handling code here:;
        int id = Integer.parseInt(txtID.getText()) ;
        String user = txtUSERNAME.getText();
        String pass = txtPASSWORD.getText();
        String personID = txtPERSONID.getText();
        String fname = txtFNAME.getText();
        String lname = txtLNAME.getText();
        int role = 0;
        if(selectedROLE == 0) {
            role = 0;
        }else{
            role = cbROLE.getSelectedIndex() + 1;
        }
        
        tempAccount = new CLASS_ACCOUNTS(id, user, pass, personID, fname, lname, role);
        if(btnACTION.getText() == "ADD ACCOUNT") {
            int Confirmation = JOptionPane.showConfirmDialog(rootPane, "ARE YOU SURE YOU WANT TO ADD NEW USER?", "CONFIRMATION", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(Confirmation == 0) { 
                try {
                    daoACCOUNTS.addAccount(tempAccount);
                    resetState();
                    btnVIEWACC.doClick();
                } catch (SQLException ex) {
                    Logger.getLogger(ADMIN.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else if(btnACTION.getText() == "UPDATE ACCOUNT") {
            int Confirmation = JOptionPane.showConfirmDialog(rootPane, "ARE YOU SURE YOU WANT TO UPDATE USER?", "CONFIRMATION", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(Confirmation == 0)  {
                try {
                    daoACCOUNTS.updateAccount(tempAccount);
                    resetState();
                    btnVIEWACC.doClick();
                } catch (SQLException ex) {
                    Logger.getLogger(ADMIN.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
        
        
    }//GEN-LAST:event_btnACTIONActionPerformed

