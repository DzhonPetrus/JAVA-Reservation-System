    private void tblACCOUNTSMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblACCOUNTSMouseReleased
        // TODO add your handling code here:
        // get selected account
        int row = tblACCOUNTS.getSelectedRow();
        // make sure a row is selected
        if(row < 0) {
            btnUPDATE.setEnabled(false);
            btnDELETE.setEnabled(false);
        }else{
            btnUPDATE.setEnabled(true);
            btnDELETE.setEnabled(true);
            
            // GET VALUE OF SELECTED ROW
            selectedID = Integer.parseInt(String.valueOf(tblACCOUNTS.getValueAt(row, TableModelACCOUNTS.ID_COL))) ;
            selectedUSER = String.valueOf(tblACCOUNTS.getValueAt(row, TableModelACCOUNTS.USERNAME_COL));
            selectedPASS = String.valueOf(tblACCOUNTS.getValueAt(row, TableModelACCOUNTS.PASSWORD_COL));
            selectedPERSONID = String.valueOf(tblACCOUNTS.getValueAt(row, TableModelACCOUNTS.PERSON_ID_COL));
            selectedFNAME = String.valueOf(tblACCOUNTS.getValueAt(row, TableModelACCOUNTS.FNAME_COL));
            selectedLNAME = String.valueOf(tblACCOUNTS.getValueAt(row, TableModelACCOUNTS.LNAME_COL));
            selectedROLE = Integer.parseInt(String.valueOf(tblACCOUNTS.getValueAt(row, TableModelACCOUNTS.ROLE_COL)));
            
        }
    }//GEN-LAST:event_tblACCOUNTSMouseReleased

